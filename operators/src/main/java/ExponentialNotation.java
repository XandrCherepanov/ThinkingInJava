/**
 * Exercise 9: (1) Display the largest and smallest numbers for both float and
 * double exponential notation.
 */

public class ExponentialNotation {
    public static void main(String[] args) {
        System.out.println("Largest float number: " + Float.MAX_VALUE);
        System.out.println("Smallest float number: " + Float.MIN_VALUE);

        System.out.println("Largest double number: " + Double.MAX_VALUE);
        System.out.println("Smallest double number: " + Double.MIN_VALUE);
    }
}
